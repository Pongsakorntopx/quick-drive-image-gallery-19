// Helper function to get translation
export const getTranslation = (key: string, language: string = "th", params?: Record<string, string | number>): string => {
  const translation = translations[key];
  if (!translation) return key;

  let text = translation[language as "th" | "en"] || translation.th;
  
  // Replace parameters
  if (params) {
    Object.entries(params).forEach(([key, value]) => {
      text = text.replace(`{${key}}`, value.toString());
    });
  }
  
  return text;
};
